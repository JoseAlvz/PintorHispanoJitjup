<p>Diego Rivera ​ fue un destacado muralista mexicano de ideología comunista, famoso por plasmar
obras de alto contenido político y social en edificios públicos.</p>
<p>Diego Rivera nació en Guanajuato, México el 8 de diciembre de 1886. Este fue uno de los artistas 
mexicanos que conformaban la tríada de los máximos representantes del muralismo mexicano, junto 
con David Alfaro Siqueiros (1896-1974) y José Clemente Orozco (1883-1949).</p>
<h2>PRIMEROS AÑOS DE EDAD E INICIOS ARTÍSTICOS</h2>
<p>Rivera era hijo de María del Pilar Barrientos y Diego Rivera Acosta. Cuando apenas tenía año y 
medio de edad, su hermano José Carlos María Barrientos Rivera murió. 
A pesar de que Diego sobrevivió, este estaba bastante débil y padecía raquitismo. <bZr>

A pesar de que el nombre real de Diego Rivera era “José Diego Rivera”, él insistía en que su nombre era 
“Diego María de la Concepción Juan Nepomuceno Estanislao de la Rivera y Barriento Acosta y Rodríguez”.
Cinco años después, cuando ya Rivera teniendo 6 años de edad, sus padres se mudarían a la Ciudad de México junto con él. <br>

Esto lo llevaría a un estudio temprano de las artes en la academia San Carlos (“Escuela de Bellas Artes de San Carlos” en la actualidad).
Es aquí donde Diego conoció a José María Velasco, un pintor paisajista que marco las enseñanzas básicas naturalistas que influenciaron
directamente en las primeras obras de Rivera.</p>
<h2>A LA CIUDAD ETERNA</h2>
<p>De las calles de la ciudad se dice que adoptó su particular estilo tenebrista. Fue un joven agresivo, impulsivo y de doble moral.
En 1592, arribó a Roma a trabajar con Giuseppe Cesari, pintando obras de frutos y flores, designados por el Papa Clemente VIII. <br>
Caravaggio deseaba pintar otro tipo de obras; entonces el pintor Prospero Orsi, lo guía hacia el movimiento católico de la 
Contrarreforma. El cardenal Francesco María del Monte buscó a Caravaggio para que decorar una de las capillas de la iglesia 
de san Luis de los Franceses. <br>
Así Caravaggio pintó a los santos a partir de personas común y con sus imperfecciones y expresiones de sufrimiento. Solo sobreviven 
unas 50 pinturas de Caravaggio, algunas de ellas son: Niño con un cesto de frutas (1593), La vocación de San Mateo (1601), 
Los discípulos de Emaús (1601), La captura de Cristo (1602).</p>
<h2>ESTUDIOS EN EUROPA</h2>
<p>En 1905, Rivera recibió una pensión del Secretario de Educación de México. Dos años más tarde, recibió una segunda pensión, 
esta vez de parte del Gobernador de Veracruz, la cual le permitió viajar a España para estudiar obras.

La firma de Rivera era una rana. Según se cree, esta podría basarse en el nombre de la ciudad donde éste nació.

Luego de recorrer varios países de Europa, los intereses de Diego cambiarían. De hecho, durante esta etapa, el pasaría del 
academicismo al arte de vanguardia. También hay información sobre el interés de Rivera por el cubismo. <br>
</p>
<h2>DE VUELTA EN MÉXICO</h2>
<p>En 1922 Diego volvió a México para estudiar de lleno la cultura maya y arte azteca. Este mismo año, fue uno de los organizadores
 de la Unión de Trabajadores Técnicos y Plásticos.
Por otro lado, según la biografía de Diego Rivera, a finales del año este iniciaría la pintura en la Secretaría de Educación
Pública. De la misma forma, este fue uno de los fundadores del Partido Comunista Mexicano.
El primer mural de Diego Rivera se llamó “La Creación” y estaba ubicado en el interior del Anfiteatro Simón Bolívar 
de la Universidad Nacional de México. <br>
Este mismo año, Rivera se casó con Guadalupe Marín y llevaría a cabo pinturas en varios edificios estatales.
Una vez divorciado de Guadalupe Marín, este se casó con Frida Kahlo en 1929, con quien tendría 10 años de matrimonio.
Falleció en su casa con 70 años el 24 de noviembre de 1957, en San Ángel, Ciudad de México.</p>
