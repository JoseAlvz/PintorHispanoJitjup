<p>Francisco de Goya, nació en España, en la provincia de Zaragoza en 1746. Hijo de un artesano muy 
conocido y quien gozaba de buena reputación, tuvo cinco hermanos. Su devoción por la pintura desde muy 
chico salió a flote y su imaginación dejaba volar en cada uno de los cuadros que empezó a crear; 
resistiéndose así a los deseos de su padre, quien quería que siguiera sus pasos. <br>
Como estudiante no fue muy destacado en el colegio; razón por la cual, a los catorce años inicia su 
formación artística; el pintor José Luzán, oriundo de su pueblo, fue el primer maestro que lo enseñó 
a perfeccionar sus trazos. Durante cuatros años estuvo a su lado, dibujos y estampas que diariamente 
iba mejorando, hasta que decidió emprender su propio camino y abrirse paso al futuro. <br>
Quiso ingresar en el año 1763 a la Academia de San Fernando en Madrid, pero fue un intento fallido. 
Sin embargo, no perdió su tiempo, pues allí conoció a Francisco Bayeu, un experimentado artista quien
lo ayudó en toda su trayectoria como pintor y lo motivó para conseguir trabajos importantes y 
fundamentales para su creación como artista, fortaleciendo a Goya en su brillante carrera. <br>
En 1773 se casó con la hermana de su amigo Francisco, Josefa Bayeu; con ella tuvo ocho hijos, de 
los cuales solo uno de ellos sobrevivió al parto y se convirtió así, en el exclusivo, único y futuro
 heredero de Goya. <br>
 <h2>GOYA PINTOR DE GRAN TALENTO</h2>
 El rey Carlos IV en 1970, lo designó pintor oficial del palacio y gracias a ello, muchas de sus obras
se conservan intactas, en el Museo del Prado de Madrid. <br>
Entre las obras que se encuentran expuestas en tan importante lugar, están: <br>
La maja vestida y la maja desnuda, La carga de mamelucos y Saturno devorando a un hijo, 
entre muchas otras. <br>
Al morir su esposa Josefa, se vinculó amorosamente con una sirvienta a quien le llevaba 35 años, dicha
 relación se mantuvo hasta el 16 de abril de 1828, día en el que murió Goya; nunca dejó de cultivar su 
 amor por la pintura, algo que cumplió a cabalidad hasta su último día de vida.  La evolución de su 
 carrera dejó plasmada en algunas de sus obras, gran herencia digna de apreciar. 
