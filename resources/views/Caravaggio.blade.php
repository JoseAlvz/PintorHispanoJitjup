<p>Michelangelo Merisi Da Caravaggio vió la luz del mundo por primera vez en la ciudad de Milán, 
región de Lombardía, Italia, el 29 de septiembre de 1571. Su padre Fermo Merisi, laboraba como 
regente y decorador del Marqués de Caravaggio; su madre Lucía Aratori pertenecía a una familia rica. <br>
En 1576 la familia se trasladó a Caravaggio porque la peste destruía a Milán. El 
padre de Caravaggio murió en 1577. Cuando Michelangelo iniciaba su interés artístico, llegaba la decadencia
 del Renacimiento. Caravaggio es reconocido como el primer gran exponente del barroco. <br>
 En 1584 se inició como aprendiz del pintor Simone Peterzano, quien fuese discípulo de Tiziano.
Concluido el aprendizaje, frecuento el eje Milán-Caravaggio, y quizás visitó Venecia, observando las obras 
de Giorgione y de Tiziano. Caravaggio aprendió el arte de la pintura manierista.
</p>
<h2>A LA CIUDAD ETERNA</h2>
<p>De las calles de la ciudad se dice que adoptó su particular estilo tenebrista. Fue un joven 
agresivo, impulsivo y de doble moral. En 1592, arribó a Roma a trabajar con Giuseppe Cesari, pintando 
obras de frutos y flores, designados por el Papa Clemente VIII. <br>
Caravaggio deseaba pintar otro tipo de obras; entonces el pintor Prospero Orsi, lo guía hacia
el movimiento católico de la Contrarreforma. El cardenal Francesco María del Monte buscó a Caravaggio
para que decorar una de las capillas de la iglesia de san Luis de los Franceses. <br>
Así Caravaggio pintó a los santos a partir de personas común y con sus imperfecciones y expresiones 
de sufrimiento. Solo sobreviven unas 50 pinturas de Caravaggio, algunas de ellas son: Niño con un 
cesto de frutas (1593), La vocación de San Mateo (1601), Los discípulos de Emaús (1601), La captura
de Cristo (1602).</p>

