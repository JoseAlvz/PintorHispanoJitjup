<P>Frida Kahlo, cuyo nombre de pila es Magdalena Carmen Frida Kahlo Calderón, fue una artista 
plástica mexicana de ascendencia alemana, nacida en Coyoacán, el 6 de julio de 1907.</P>
<P>Estuvo casada con el también pintor Diego Rivera con quien sostuvo una relación tormentosa y apasionada
que los involucraba no solo con el arte sino, en la política de su tiempo. Frida, estuvo toda su vida 
signada por la enfermedad y el acecho de la muerte.
Durante su infancia, sufrió de poliomielitis, dejándola maltrecha en sus extremidades inferiores,
luego, un accidente de tránsito terminó de maltratar su existencia teniendo que vivir dependiente
 de los demás para sus necesidades básicas. <br>
Aún así pudo sobreponerse a una vida limitada de motricidad independiente, pero no de imaginación y 
creatividad. Frida es un personaje con una vida llena de dolor y misterio que fue plasmada en sus 
pinturas en donde el autorretrato es su principal motivo.</P>
<h2>COMIENZOS EN LA PINTURA</h2>
<p>A raíz del accidente sufrido a los dieciocho años, que le fracturó la pelvis y la columna vertebral, 
Frida debió guardar un reposo absoluto, y es ahí, cuando comienza su aprendizaje de la pintura para 
combatir el tedio de la inmovilidad. <br>
Comenzó pintándose a sí misma, con todas las dificultades derivadas de su condición física, sin embargo,
buscaron la forma de que pudiera pintar, aún acostada, con la ayuda del espejo y un caballete diseñado 
especialmente para ella. <br>
Al principio, Frida experimentó con acuarela, luego pasó al óleo. Su primer trabajó lo tituló: 
Autorretrato con traje de terciopelo, fue un regalo para su entonces novio, Alejandro Gómez, quien 
la abandonó luego del accidente. <br>
Esta convalecencia fue el inicio de una trayectoria artística en la que, predominan los colores, los 
símbolos y las representaciones estéticas de la mexicanidad, compartidas con otros artistas de la época,
como su esposo Diego Rivera, Siqueiros, Clemente Orozco y otros.</p>
<h2>ESTILO Y ESTÉTICA EN LA PINTURA DE FRIDA KAHLO</h2>
<p>Lo que más se destaca en la pintura de Kahlo, es el autorretrato como una forma de autoreafirmación de
 su personalidad y de evasión ante el infortunio que le tocó vivir en relación con su salud, su vida 
 sentimental y con los anhelos frustrados de maternidad. <br>
Algunos de los títulos de sus cuadros son: Frida Kahlo repartiendo armas, El tiempo vuela, Frida y Diego 
Rivera, Lo que el agua me dio. Todas estas obras hacen alusión metafóricamente a su vida llena de 
contradicciones. <br>
La pintura de Frida Kahlo, fue considerada por André Breton como Surrealista, aunque ella no lo creyera 
así, pero fue de tal impacto el cuadro Lo que el agua me dio, en Bretón, que este le ofreció exponer en la 
ciudad de París, comenzando así, una nueva etapa en su vida artística. <br>
Desde este momento las exposiciones no pararon, se mantuvo entre París, Ciudad de México y Nueva York, 
produciendo enormes sumas de dinero. A partir de 1946, la salud de Frida comenzó a empeorar y fallece el
13 de julio de 1954, en la Casa Azul, en la Ciudad de Coyoacán.
</p>
