@section ('content')
<p>Rembrandt Harmenszoon van Rijn nació el 15 de julio de 1606 en Leiden, Holanda. Fue el octavo y 
penúltimo de sus hermanos. Es uno de los máximos exponentes del barroco en el siglo de oro holandés. <br>
Rembrandt era hijo de molineros adinerados, por lo que tuvo acceso al estudio en la Escuela Latina y 
a la universidad de Leiden en 1620 con solo 14 años; sin embargo, desechó sus estudios para hacerse 
pintor en el taller Jacob van Swanenburch, pintor de Leiden.</p> 
<h2>PINTOR INDEPENDIENTE</h2>
<p>Ya en 1624, se trasladó para Ámsterdam al taller del famoso artista Pieter Lastman, donde se ve 
influenciado por la pintura del Caravaggio. En 1625 Rembrandt consideró el tiempo justo para hacer su 
propio taller en Leiden y se asoció con Jan Lievens, su compañero de clases. <br>
En esa época crea la obra La lapidación de San Esteban, lo que le genera aprobación y fama. En 1630 
retorna a Amsterdam, luego de fallecido su padre, recibiendo grandes pedidos del marchante Hendrick 
van Uylenburgh, asociándose con este. <br>
En 1632 aparece La lección de anatomía del doctor Tulp, que lo puso en la cima y le lleno de trabajo. 
Su reconocimiento como gran retratista, detallista y con una capacidad única de resaltar las cualidades
del modelo le dan un estatus económico y social único en su época. <br>
En 1634 Rembrandt se casa con Saskia van Uylenburgh, sobrina de Hendrick, de familia muy rica. Saskia se 
transformó en el punto de apoyo, la modelo e inspiración. Está presente en: El banquete nupcial de Sansón
(1638), El sacrificio de Isaac (1635), Saskia con sombrero (1635), entre otros.
</p>
<h2>DE LA GLORIA A LA TRAGEDIA</h2>
<p>Parecía que su vida llevaba un excelente rumbo hasta que entre 1635 y 1641 pierde tres hijos y en 1642,
cuando tenía seis meses su único hijo sobreviviente Titus, su esposa Saskia fallece víctima de tuberculosis,
 acentuándose el inicio del declive de Rembrandt. <br>
Se anexan problemas económicos, una denuncia de la nana de Titus por incumplir su promesa de matrimonio, 
siendo juzgado por su relación de concubinato con su doméstica, en medio de sociedad severa y clasista. 
Esta joven Heindrickje Stofeels, será su acompañante y madre de su hija Cornelia. <br>
Rembrandt continúa creando, transformando su estilo del exuberante barroco a uno de muchos detalles, con
vivaces coloridos, modelos más humanos y una temática de religiosidad holandesa. Manejó con suprema 
maestría la pintura al óleo, el claroscuro y el dibujo en sus grabados. <br>
Hacia la década de 1660 regresan las comisiones, así pinta Los síndicos de los pañeros (1662), La novia 
judía (1666) y la innovadora Lección de anatomía del doctor Leyman (1656). Rembrandt eleva su reputación,
realizando obras con un procedimiento muy particular, a través de pinceladas largas, claroscuros, efectos 
de luz dorada, toque estilístico de Tiziano.
Lamentablemente muere su hijo Titus en 1668. Rembrandt muere el 4 de octubre de 1669, en Amsterdam, sin
poder acoplarse a su tiempo, pero iluminando el camino del arte. Fue autor, además, de unos 400 grabados 
y 1.500 dibujos.</p>
